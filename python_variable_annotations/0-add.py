#!/usr/bin/python3

def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the sum.

    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.

    Returns:
    float: The sum of the two input numbers.
    """
    # Adding the two input numbers
    result = a + b

    # Returning the sum as a float
    return result
