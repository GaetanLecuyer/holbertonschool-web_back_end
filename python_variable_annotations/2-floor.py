#!/usr/bin/env python3
"""
Task 2:
Returns the largest integer less than or equal to the input number.
"""
import math


def floor(n: float) -> float:
    """
    Returns the floor of a given float.

    Args:
        n (float): The input float.

    Returns:
        float: The floor of the input float.
    """
    return math.floor(n)
